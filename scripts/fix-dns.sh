#!/bin/bash

# Fix DNS Resolution for Test Environment
# This script configures the system to use public DNS servers

echo "Fixing DNS resolution..."

# Check current DNS configuration
echo "Current DNS configuration:"
cat /etc/resolv.conf 2>/dev/null || echo "No /etc/resolv.conf found"

# Try to use systemd-resolved if available
if command -v systemd-resolve >/dev/null 2>&1; then
    echo "Using systemd-resolved..."
    sudo systemd-resolve --set-dns=8.8.8.8 --interface=eth0 2>/dev/null || true
    sudo systemd-resolve --set-dns=8.8.4.4 --interface=eth0 2>/dev/null || true
fi

# Alternative: Create temporary resolv.conf
if [ -w /etc/resolv.conf ]; then
    echo "Updating /etc/resolv.conf..."
    cat > /tmp/resolv.conf.tmp <<EOF
# Generated by fix-dns.sh
nameserver 8.8.8.8
nameserver 8.8.4.4
nameserver 1.1.1.1
EOF
    sudo cp /tmp/resolv.conf.tmp /etc/resolv.conf 2>/dev/null || cp /tmp/resolv.conf.tmp /etc/resolv.conf
    echo "DNS configuration updated"
else
    echo "Cannot write to /etc/resolv.conf"
    echo "You may need to run this script with sudo or configure DNS manually"
fi

# Test DNS resolution
echo ""
echo "Testing DNS resolution..."
if host google.com >/dev/null 2>&1; then
    echo "✓ DNS resolution is working"
else
    echo "✗ DNS resolution is still not working"
    echo ""
    echo "Alternative solutions:"
    echo "1. Use Go module proxy: export GOPROXY=direct"
    echo "2. Configure Docker with DNS: docker run --dns 8.8.8.8"
    echo "3. Use offline mode: export GO_OFFLINE=1"
fi

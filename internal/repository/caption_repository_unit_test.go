func setupCaptionMockDB(t *testing.T) (*sqlx.DB, sqlmock.Sqlmock) {
	t.Helper()

	mockDB, mock, err := sqlmock.New()
	require.NoError(t, err)

	return sqlx.NewDb(mockDB, "sqlmock"), mock
}

func newCaptionRepo(t *testing.T) (*captionRepository, sqlmock.Sqlmock, func()) {
	t.Helper()

	db, mock := setupCaptionMockDB(t)
	repo := NewCaptionRepository(db)
	cleanup := func() { _ = db.Close() }
	return repo.(*captionRepository), mock, cleanup
}

func sampleCaption() *domain.Caption {
	now := time.Now()
	filePath := "/data/captions/test.vtt"
	ipfsCID := "QmTestCID123"
	fileSize := int64(4096)
	return &domain.Caption{
		ID:              uuid.New(),
		VideoID:         uuid.New(),
		LanguageCode:    "en",
		Label:           "English",
		FilePath:        &filePath,
		IPFSCID:         &ipfsCID,
		FileFormat:      domain.CaptionFormatVTT,
		FileSizeBytes:   &fileSize,
		IsAutoGenerated: false,
		CreatedAt:       now,
		UpdatedAt:       now,
	}
}

func makeCaptionScanRows(c *domain.Caption) *sqlmock.Rows {
	return sqlmock.NewRows([]string{
		"id", "video_id", "language_code", "label", "file_path", "ipfs_cid",
		"file_format", "file_size_bytes", "is_auto_generated", "created_at", "updated_at",
	}).AddRow(
		c.ID, c.VideoID, c.LanguageCode, c.Label, c.FilePath, c.IPFSCID,
		c.FileFormat, c.FileSizeBytes, c.IsAutoGenerated, c.CreatedAt, c.UpdatedAt,
	)
}


func TestCaptionRepository_Unit_GetByVideoID(t *testing.T) {
	ctx := context.Background()
	caption := sampleCaption()

	t.Run("success with results", func(t *testing.T) {
		repo, mock, cleanup := newCaptionRepo(t)
		defer cleanup()

		cap2 := sampleCaption()
		cap2.ID = uuid.New()
		cap2.VideoID = caption.VideoID
		cap2.LanguageCode = "es"
		cap2.Label = "Spanish"

		rows := makeCaptionScanRows(caption)
		rows.AddRow(
			cap2.ID, cap2.VideoID, cap2.LanguageCode, cap2.Label, cap2.FilePath, cap2.IPFSCID,
			cap2.FileFormat, cap2.FileSizeBytes, cap2.IsAutoGenerated, cap2.CreatedAt, cap2.UpdatedAt,
		)

		mock.ExpectQuery(`(?s)SELECT id, video_id, language_code, label, file_path, ipfs_cid.*FROM captions.*WHERE video_id = \$1.*ORDER BY language_code`).
			WithArgs(caption.VideoID).
			WillReturnRows(rows)

		got, err := repo.GetByVideoID(ctx, caption.VideoID)
		require.NoError(t, err)
		require.Len(t, got, 2)
		assert.Equal(t, caption.ID, got[0].ID)
		assert.Equal(t, cap2.ID, got[1].ID)
		require.NoError(t, mock.ExpectationsWereMet())
	})

	t.Run("empty result returns empty slice", func(t *testing.T) {
		repo, mock, cleanup := newCaptionRepo(t)
		defer cleanup()

		rows := sqlmock.NewRows([]string{
			"id", "video_id", "language_code", "label", "file_path", "ipfs_cid",
			"file_format", "file_size_bytes", "is_auto_generated", "created_at", "updated_at",
		})

		mock.ExpectQuery(`(?s)SELECT id, video_id, language_code, label, file_path, ipfs_cid.*FROM captions.*WHERE video_id = \$1.*ORDER BY language_code`).
			WithArgs(caption.VideoID).
			WillReturnRows(rows)

		got, err := repo.GetByVideoID(ctx, caption.VideoID)
		require.NoError(t, err)
		require.NotNil(t, got)
		assert.Empty(t, got)
		require.NoError(t, mock.ExpectationsWereMet())
	})

	t.Run("query failure", func(t *testing.T) {
		repo, mock, cleanup := newCaptionRepo(t)
		defer cleanup()

		mock.ExpectQuery(`(?s)SELECT id, video_id, language_code, label, file_path, ipfs_cid.*FROM captions.*WHERE video_id = \$1`).
			WithArgs(caption.VideoID).
			WillReturnError(errors.New("query failed"))

		got, err := repo.GetByVideoID(ctx, caption.VideoID)
		require.Nil(t, got)
		require.Error(t, err)
		assert.Contains(t, err.Error(), "failed to get captions")
		require.NoError(t, mock.ExpectationsWereMet())
	})
}


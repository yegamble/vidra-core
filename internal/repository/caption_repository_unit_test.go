func TestCaptionRepository_Unit_Create(t *testing.T) {
	ctx := context.Background()

	t.Run("success", func(t *testing.T) {
		repo, mock, cleanup := newCaptionRepo(t)
		defer cleanup()

		caption := sampleCaption()
		returnedID := uuid.New()

		mock.ExpectQuery(`(?s)INSERT INTO captions`).
			WithArgs(
				sqlmock.AnyArg(), // id (may be regenerated)
				caption.VideoID, caption.LanguageCode, caption.Label,
				caption.FilePath, caption.IPFSCID, caption.FileFormat, caption.FileSizeBytes,
				caption.IsAutoGenerated, sqlmock.AnyArg(), // created_at
			).
			WillReturnRows(sqlmock.NewRows([]string{"id"}).AddRow(returnedID))

		err := repo.Create(ctx, caption)
		require.NoError(t, err)
		assert.Equal(t, returnedID, caption.ID)
		assert.False(t, caption.CreatedAt.IsZero())
		assert.False(t, caption.UpdatedAt.IsZero())
		require.NoError(t, mock.ExpectationsWereMet())
	})

	t.Run("generates uuid when nil", func(t *testing.T) {
		repo, mock, cleanup := newCaptionRepo(t)
		defer cleanup()

		caption := sampleCaption()
		caption.ID = uuid.Nil
		returnedID := uuid.New()

		mock.ExpectQuery(`(?s)INSERT INTO captions`).
			WithArgs(
				sqlmock.AnyArg(), // newly generated id
				caption.VideoID, caption.LanguageCode, caption.Label,
				caption.FilePath, caption.IPFSCID, caption.FileFormat, caption.FileSizeBytes,
				caption.IsAutoGenerated, sqlmock.AnyArg(),
			).
			WillReturnRows(sqlmock.NewRows([]string{"id"}).AddRow(returnedID))

		err := repo.Create(ctx, caption)
		require.NoError(t, err)
		assert.Equal(t, returnedID, caption.ID)
		require.NoError(t, mock.ExpectationsWereMet())
	})

	t.Run("query failure", func(t *testing.T) {
		repo, mock, cleanup := newCaptionRepo(t)
		defer cleanup()

		caption := sampleCaption()

		mock.ExpectQuery(`(?s)INSERT INTO captions`).
			WithArgs(
				sqlmock.AnyArg(),
				caption.VideoID, caption.LanguageCode, caption.Label,
				caption.FilePath, caption.IPFSCID, caption.FileFormat, caption.FileSizeBytes,
				caption.IsAutoGenerated, sqlmock.AnyArg(),
			).
			WillReturnError(errors.New("insert failed"))

		err := repo.Create(ctx, caption)
		require.Error(t, err)
		assert.Contains(t, err.Error(), "failed to create caption")
		require.NoError(t, mock.ExpectationsWereMet())
	})
}


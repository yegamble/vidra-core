package domain

import (
	"time"

	"github.com/google/uuid"
)

// CaptionFormat represents the subtitle file format
type CaptionFormat string

const (
	CaptionFormatVTT CaptionFormat = "vtt" // WebVTT
	CaptionFormatSRT CaptionFormat = "srt" // SubRip
)

// Caption represents a video caption/subtitle track
type Caption struct {
	ID              uuid.UUID     `json:"id" db:"id"`
	VideoID         uuid.UUID     `json:"video_id" db:"video_id"`
	LanguageCode    string        `json:"language_code" db:"language_code"` // e.g., 'en', 'en-US', 'es'
	Label           string        `json:"label" db:"label"`                 // e.g., 'English', 'Spanish'
	FilePath        *string       `json:"-" db:"file_path"`                 // Internal, not exposed
	IPFSCID         *string       `json:"ipfs_cid,omitempty" db:"ipfs_cid"`
	FileFormat      CaptionFormat `json:"file_format" db:"file_format"`
	FileSizeBytes   *int64        `json:"file_size_bytes,omitempty" db:"file_size_bytes"`
	IsAutoGenerated bool          `json:"is_auto_generated" db:"is_auto_generated"`
	CreatedAt       time.Time     `json:"created_at" db:"created_at"`
	UpdatedAt       time.Time     `json:"updated_at" db:"updated_at"`

	// Computed field for API response
	URL string `json:"url,omitempty" db:"-"` // Generated download URL
}

// CreateCaptionRequest represents a request to create/upload a caption
type CreateCaptionRequest struct {
	LanguageCode    string        `json:"language_code" validate:"required,min=2,max=10"`
	Label           string        `json:"label" validate:"required,min=1,max=100"`
	FileFormat      CaptionFormat `json:"file_format" validate:"required,oneof=vtt srt"`
	IsAutoGenerated bool          `json:"is_auto_generated"`
}

// UpdateCaptionRequest represents a request to update a caption (make subtitles editable)
type UpdateCaptionRequest struct {
	Label        *string `json:"label,omitempty" validate:"omitempty,min=1,max=100"`
	LanguageCode *string `json:"language_code,omitempty" validate:"omitempty,min=2,max=10"`
}

// CaptionListResponse represents a list of captions for a video
type CaptionListResponse struct {
	Captions []Caption `json:"captions"`
	Count    int       `json:"count"`
}

// IsValid checks if the caption format is valid
func (f CaptionFormat) IsValid() bool {
	return f == CaptionFormatVTT || f == CaptionFormatSRT
}

// String returns the string representation of the caption format
func (f CaptionFormat) String() string {
	return string(f)
}

// GetContentType returns the MIME type for the caption format
func (f CaptionFormat) GetContentType() string {
	switch f {
	case CaptionFormatVTT:
		return "text/vtt"
	case CaptionFormatSRT:
		return "application/x-subrip"
	default:
		return "text/plain"
	}
}
